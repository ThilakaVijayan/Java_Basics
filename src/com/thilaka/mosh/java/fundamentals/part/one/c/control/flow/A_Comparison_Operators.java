package com.thilaka.mosh.java.fundamentals.part.one.c.control.flow;

public class A_Comparison_Operators {
    public static void main(String[] args) {
        int x = 1; //single '=' is called assignment operator
        int y = 1;
        System.out.println(x == y);//Equality operator
        System.out.println(x != y);//Inequality operator
        System.out.println(x > y);
        System.out.println(x >= y);
        System.out.println(x < y);
        System.out.println(x <= y);
        /**
         * ======== OUTPUT ===========
         * true
         * false
         * false
         * true
         * false
         * true
         */
    }
}
